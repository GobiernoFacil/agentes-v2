		<h2>Contexto</h2>
		<p>El 7 de julio de 2015, el estado de San Luis Potosí se integró formalmente a los Ejercicios Locales de Gobierno Abierto a través de la firma de la <a href="http://inicio.ifai.org.mx/doc/DGGAT/LGTAP70XLVIIID/FirmaDeclaratoriaSanLuisreu.pdf">Declaración Conjunta para la Implementación de Acciones para un Gobierno Abierto</a>.</p>
		<p>En esta Declaración, firmada por dos representantes del Gobierno del Estado, un representante de la sociedad civil y el Organismo Garante de Acceso a la Información y Protección de Datos Personales en la entidad, San Luis Potosí se comprometió a establecer actividades y trabajos encaminados a consolidar y expandir la apertura institucional y la participación ciudadana.</p>
		<p>Luego de la firma de la Declaración Conjunta, en San Luis Potosí se impartieron tres sesiones de sensibilización con organizaciones de la sociedad civil y público en general, donde se informó y socializó el ejercicio Gobierno Abierto “Co-creación desde lo Local” y se capacitó a servidores públicos que mostraron interés hacia el proyecto.</p>
		<p>Posteriormente, el 26 de febrero de 2016 se instaló el Secretariado Técnico en la entidad, mismo que coordinó el proceso de construcción del primer Plan de Acción en la entidad, consistente en una consulta pública, un foro de expertos, la integración de un Comité de Planeación del Desarrollo del Estado y la celebración de mesas de diálogo en las cuales intervinieron representantes de la sociedad civil, autoridades gubernamentales y el propio Organismo Garante.</p>
		<p>Este proceso dio como resultado que el 11 de mayo de 2017 se publicara el <a href="http://inicio.ifai.org.mx/doc/DGGAT/LGTAP70XLVIIID/PAL%2520San%2520Luisreu.pdf">Plan de Acción Local</a>. El documento está integrado por 5 compromisos puntuales, realizables, relevantes y medibles, con duración de un año:</p>
		<ul>
			<li><strong>Compromiso 1:</strong> Implementar un sistema digital que, por medio de indicadores y alertas, reporte el avance de obra pública de programas sociales federales, con el fin de lograr su difusión, evaluación y seguimiento del mismo, por parte de los beneficiarios y los comités de contraloría social.</li>
			<li><strong>Compromiso 2:</strong> Ejecutar un programa social a partir de las propuestas de los Clubes Rotarios de la entidad para trabajar conjuntamente con el Gobierno del Estado, con el propósito de fortalecer acciones específicas de desarrollo social comunitario. Se propone que por cada peso y acción que aporten las OSC, el gobierno aportará otro peso y otra acción en beneficio del proyecto seleccionado.</li>
			<li><strong>Compromiso 3:</strong> Incrementar el porcentaje de participación ciudadana de la población beneficiaria para la vigilancia de programas sociales y de infraestructura, con el fin de proponer mejoras al esquema estatal de contraloría social y constituir una Comisión Permanente de Contraloría Social en el Estado.</li>
			<li><strong>Compromiso 4:</strong> Funcionar como una incubadora para desarrollar e implementar proyectos de gobierno abierto a partir de los principios de participación ciudadana, transparencia, rendición de cuentas e innovación gubernamental y tecnológica.</li>
			<li><strong>Compromiso 5:</strong> Impulsar la participación ciudadana en el ámbito municipal a través de difusión y capacitación a funcionarios públicos municipales en materia de Contraloría Social con el fin de contribuir a que la gestión gubernamental y el manejo de los recursos públicos se realicen en términos de transparencia, eficacia, legalidad y honradez promoviendo entre servidores públicos y ciudadanía la rendición de cuentas.</li>
		</ul>
		
