		<h2>Contexto</h2>
			<p>El 9 de marzo de 2015, el estado de Durango se integró formalmente a los Ejercicios Locales de Gobierno Abierto a través de la firma de la <a href="http://inicio.ifai.org.mx/doc/DGGAT/LGTAP70XLVIIID/FirmaDeclaratoriaDurangoreu.pdf">Declaración Conjunta para la Implementación de Acciones para un Gobierno Abierto</a>.</p>
			<p>En esta Declaración, firmada por un representante de gobierno municipal, un representante de la sociedad civil y el Organismo Garante de Acceso a la Información y Protección de Datos Personales en la entidad, Durango se comprometió a establecer actividades y trabajos encaminados a consolidar y expandir la apertura institucional y la participación ciudadana.</p>
			<p>Luego de la firma de la Declaración Conjunta, en Durango se impartió una sesión de sensibilización con organizaciones de la sociedad civil y público en general, donde se informó y socializó el ejercicio Gobierno Abierto “Co-creación desde lo Local” y se capacitó a servidores públicos que mostraron interés hacia el proyecto.</p>
			<p>Posteriormente, el 10 de abril de 2015 se instaló el Secretariado Técnico en la entidad, mismo que coordinó la celebración de ocho mesas de trabajo en el mes de mayo de 2015, en las cuales intervinieron representantes de la sociedad civil, autoridades gubernamentales y el propio Organismo Garante. </p>

			<p>Las mesas de trabajo se realizaron en materias de: Seguridad Pública; Servicios Públicos; Medio Ambiente; Empleo; Transparencia y Rendición de
Cuentas; Contraloría Social; Salud y Educación.</p>
			<p>Este proceso dio como resultado que el 25 de agosto de 2015 se publicara el <a href="http://inicio.ifai.org.mx/doc/DGGAT/LGTAP70XLVIIID/PALDurangoreu.pdf">Plan de Acción Local</a>. El documento está integrado por 6 compromisos puntuales, realizables, relevantes y medibles, con duración de un año:</p>
			<ul>
				<li><strong>Compromiso 1:</strong> Dar seguimiento a los recursos asignado y ejercidos en el programa "Agua Limpia" del estado, con el fin de proponer mecanismos de mejora que logren enriquecer la participación ciudadana.</li>
				<li><strong>Compromiso 2:</strong> Reducir la contaminación ocasionada por ladrilleras artesanales informales, limitando su instalación y promoviendo una cultura de manejo y disposición adecuada de residuos sólidos.</li>
				<li><strong>Compromiso 3:</strong> Promover el manejo y disposición adecuados de los residuos sólidos para disminuir la contaminación en el municipio de Durango.</li>
				<li><strong>Compromiso 4:</strong> Fortalecer los mecanismos de Contraloría Social, con acciones de Transparencia y Rendición de Cuentas que incrementen la participación ciudadana para vigilar el uso eficiente y honesto de los recursos públicos.</li>
				<li><strong>Compromiso 5:</strong> Programa integral para reducir los índices de violencia en 30 escuelas secundarias públicas.</li>
				<li><strong>Compromiso 6:</strong> Identificación de las necesidades de capacitaciones conforme a las vocaciones industriales locales y fortalecimiento en los programas de capacitación y formación.</li>
			</ul>
		<h2>Estatus</h2>
