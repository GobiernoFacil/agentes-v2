		<h2>Contexto</h2>
			<p>El 20 de junio de 2016, el Estado de México se integró formalmente a los Ejercicios Locales de Gobierno Abierto a través de la firma de la <a href="http://inicio.ifai.org.mx/doc/DGGAT/LGTAP70XLVIIID/FirmaDeclaratoriaEdoMexreu.pdf">Declaración Conjunta para la Implementación de Acciones para un Gobierno Abierto</a>.</p>
			<p>En esta Declaración, firmada por dos representantes del Gobierno del Estado, un representante de la sociedad civil y el Organismo Garante de Acceso a la Información y Protección de Datos Personales en la entidad, el Estado de México se comprometió a establecer actividades y trabajos encaminados a consolidar y expandir la apertura institucional y la participación ciudadana.</p>
			<p>Luego de la firma de la Declaración Conjunta, en el Estado de México se han impartido 2 sesiones de sensibilización con organizaciones de la sociedad civil y público en general, donde se informó y socializó el ejercicio Gobierno Abierto “Co-creación desde lo Local” y se capacitó a servidores públicos que mostraron interés hacia el proyecto, lo anterior, de cara a la instalación del Secretariado Técnico Local de la entidad.</p>
			
		<h2>Estatus</h2>
		<p>En proceso de definición de representantes de los tres sectores (sociedad civil, autoridad y organismo garante) para la integración del Secretariado Técnico Local.</p>

