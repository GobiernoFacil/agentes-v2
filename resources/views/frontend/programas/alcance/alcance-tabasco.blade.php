		<h2>Contexto</h2>
		<p>El 9 de marzo de 2015, el estado de Tabasco se integró formalmente a los Ejercicios Locales de Gobierno Abierto a través de la firma de la <a href="http://inicio.ifai.org.mx/SiteCollectionDocuments/Transparencia/Tabasco/Tabasco%2520segunda%2520Declaratoria.pdf">Declaración Conjunta para la Implementación de Acciones para un Gobierno Abierto</a>, misma que fue refrendada el 2 de marzo de 2017.</p>
		<p>En esta Declaración, firmada por un representante del Gobierno del Estado, un representante de la sociedad civil y el Organismo Garante de Acceso a la Información y Protección de Datos Personales en la entidad, Tabasco se comprometió a establecer actividades y trabajos encaminados a consolidar y expandir la apertura institucional y la participación ciudadana.</p>
		<p>Luego del relanzamiento formal del ejercicio mediante el refrendo de la Declaración Conjunta, en Tabasco se han impartido tres sesiones de sensibilización con organizaciones de la sociedad civil y público en general, donde se informó y socializó el ejercicio Gobierno Abierto “Co-creación desde lo Local” y se capacitó a servidores públicos que mostraron interés hacia el proyecto.</p>
		<p>El Secretariado Técnico en la entidad fue instalado el 14 de septiembre de 2017 y actualmente tiene la encomienda de coordinar el proceso de construcción del primer Plan de Acción en la entidad.</p>
		
