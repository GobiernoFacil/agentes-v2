		<h2>i. Gobierno Abierto: breve discusión teórica</h2>
		<p>Gobierno Abierto es un enfoque teórico que propone una forma particular de
entender los procesos de gobierno, a partir de los principios de transparencia y
participación ciudadana. Esta perspectiva se ha construido como consecuencia de
al menos tres fenómenos observados: i) la globalización, en cuanto a que algunas
dinámicas políticas nacionales – y locales - están condicionadas por factores que
trascienden las fronteras estatales; ii) el reconocimiento, cada vez más recurrente,
de que la gubernamental no es la única esfera legítima y capaz para definir y atender
las necesidades crecientemente complejas de las comunidades y iii) la búsqueda
de mecanismos político-administrativos alternativos a los tradicionales que
fortalezcan la legitimidad de los Estados, más allá de las elecciones y de las
estructuras políticas representativas. Estos tres fenómenos han llevado,
consecuentemente, a una reflexión sobre el modo como debería reconfigurarse el
quehacer público y gubernamental; esto es, el actual modelo de gobernanza.</p>
		<p>Gobierno Abierto como perspectiva, surgió del reconocimiento de los problemas de
legitimidad y capacidad que actualmente enfrentan los gobiernos para responder a
las cada vez más numerosas, diversas y complejas demandas sociales. En este
escenario, las tendencias mundiales de reforma político-administrativa de los
últimos años han apuntado hacia la incorporación de ciudadanos y organizaciones
de la sociedad civil en los procesos de planeación, ejecución y evaluación de las actividades gubernamentales. Desde esta lógica de cambio en el modelo de
gobernanza, el ciudadano es parte de la solución de los problemas públicos – y no
un problema -, y se erige como un protagonista con pleno derecho para influir en la
definición de la agenda y de las estrategias gubernamentales.</p>
		<p>En consonancia con lo anterior, Gobierno Abierto como enfoque y como propuesta
de cambio del paradigma gubernativo tradicional, es compatible con marcos
teóricos y analíticos – como el de gobernanza - que ponen el acento en la
conformación de escenarios de gobierno horizontales y con una presencia plural de
actores privados y sociales en los procesos decisionales de la política pública, en
un plano de coordinación. De igual manera, Gobierno Abierto es una perspectiva
compatible con enfoques de corte gerencial que proponen una nueva forma de
entender el quehacer público, a partir de la introducción en el sector público de
nuevas actitudes y aptitudes que permitan incorporar efectivamente al ciudadano
en la gestión pública. Desde estos enfoques, el ciudadano es visto como una pieza
fundamental en el desarrollo de esquemas que permitan una gestión dinámica y de
calidad, orientados a la creación de valor público e innovaciones constantes.</p>
		<p>Al conjugar estas dos perspectivas teóricas de las que Gobierno Abierto se nutre –
gobernanza y enfoques gerenciales -, los gobiernos dejan de ser un conjunto de
autoridades públicas unidas por un proyecto único, para transformarse en un
espacio para la producción de decisiones y de cursos de acción, que no
necesariamente está monopolizado por un conjunto limitado de actores políticos o
burocráticos. En este sentido, Aguilar agrega que:</p>
		<blockquote>“Gobernar en contextos políticos plurales y autónomos, de alta
intensidad ciudadana y con graves problemas sociales irresueltos,
parece exigir dos requisitos fundamentales: gobernar por políticas y
gobernar con sentido público. Las estrategias de gobierno homogéneas
y globales, así como los estilos de gobierno secretos, excluyentes y
clientelares, están previsiblemente condenadas en el futuro inmediato a
la ineficacia administrativa, al castigo electoral y a la hostilidad política”
(Aguilar, 1992: 30).</blockquote>
		<p>La búsqueda de mecanismos que incorporen la opinión de distintas redes de actores
en los procesos de decisión y gestión pública coadyuvan, desde el Gobierno Abierto,
a la generación de confianza entre gobernantes y gobernados. El modo particular
de entender la gobernanza democrática desde el Gobierno Abierto permite, en
términos prácticos, incorporar talento, creatividad, ideas y voluntad en la búsqueda
de soluciones a los problemas públicos considerados como relevantes dentro de
una comunidad política particular. En este ámbito, Gobierno Abierto abona a la
reconstitución y al fortalecimiento de la legitimidad política de los Estados y de los
regímenes democráticos.</p>
		<p>Más allá de estos referentes teóricos amplios, Gobierno Abierto requiere para su
conceptualización de un conjunto de marcos analíticos de apoyo que ofrezcan -
tanto al diseñador como al implementador de políticas - guías generales que
permitan su correcta aplicación. En este nivel se identifican al menos tres referentes
conceptuales relevantes: </p>
		<ol>
			<li>los provenientes de los estudios sobre las
organizaciones públicas,</li>
 			<li>los vinculados con la gestión del cambio y</li> 
 			<li>y los interesados en el estudio de la innovación social.</li>
 		</ol>
		<p><strong>Sobre las organizaciones públicas</strong>. Gobierno Abierto coloca a autoridades,
funcionarios y ciudadanos en un nivel equivalente, de armonía, en donde ninguno
de ellos es más importante que el otro en la definición de prioridades sociales. Sin
embargo, y dados los procesos organizacionales que ocurren al interior de una
entidad u organismo, debe prestarse atención a la dinámica que se presentan en
ellas. Las organizaciones cuentan con características que las diferencian entre sí y
que propician la articulación de dinámicas internas distintas. Entre ellas es posible
incluir la existencia de fronteras relativamente fijas, de un orden normativo particular,
de niveles distintos de autoridad, y de sistemas de comunicación y de incentivos
que llevan a que el trabajo conjunto entre diversos actores pueda ocurrir de distintas
maneras.</p>
		<p>La puesta en marcha de una iniciativa de Gobierno Abierto enfrentará, pues, a los
integrantes de la organización con los elementos ya establecidos e
institucionalizados de la misma. Al respecto, Hall señala que: <em>“las organizaciones
trascienden las vidas de sus miembros y tienen metas propias, como sugieren las
actividades con propósitos. Las organizaciones están diseñadas para hacer algo.
Esta idea de Weber ha sido conservada por la mayor parte de los analistas de la
organización”</em> (1996:30). También apunta que <em>“las estructuras organizacionales
sirven tres funciones. Antes que todo, las estructuras tienen la intención de elaborar
productos y alcanzar objetivos. En segundo lugar, las estructuras se diseñan para
minimizar, o por lo menos, regular, la influencia de individuos particulares sobre la
organización. Las estructuras se imponen para asegurar que los individuos se
ajusten a los requisitos de las organizaciones y, no viceversa. Por último, las
estructuras son el ambiente donde se ejerce el poder, donde se toman las
decisiones y donde se desarrollan las actividades de las organizaciones: la
estructura es la arena de las acciones organizacionales”</em> (1996:53). De aquí la
importancia del establecimiento de una estrategia para gestionar el cambio al
interior de la organización.</p>

		<p><strong>Sobre la gestión del cambio</strong>. Incorporar principios de Gobierno Abierto, como acción
o como modelo de gestión, en una organización implica también modificar el
equilibrio tradicional de trabajo dentro de una organización pública. Este cambio, sin
embargo, no se logra por decreto y, a pesar de las bondades de este tipo de iniciativas, debe establecerse un plan de acción para asegurar su correcta
asimilación dentro de las estructuras organizacionales. En este sentido, Galofré Isart
(1997) identifica seis condiciones necesarias para el éxito de la aplicación de todo
esfuerzo modernizador, que son:</p>
		<ol>
			<li>Que las estrategias de cambio elegidas sean coherentes y complementarias entre sí, además de que partan de un diagnóstico y un cálculo realista.</li>
			<li>La existencia de compromiso político.</li>
			<li>La capacidad, liderazgo y perseverancia del equipo reformador.</li>
			<li>La existencia de los medios adecuados para alcanzar los objetivos previstos.</li>
			<li>Formar e informar continuamente a los integrantes de la organizaciones que pretende reformarse.	</li>
			<li>Propiciar un clima favorable para el cambio organizacional.</li>
		</ol>
		<p>Para valorar la idoneidad de cualquier estrategia de cambio, Galofré Isart propone
también la realización de un diagnóstico previo que:</p>
		<ol>
			<li>analice la situación actual;</li>
			<li>establezca los cambios que requiere la organización para alcanzar el nivel de mejora deseado; </li>
			<li>que se definan las estrategias a realizarse considerando las capacidades de cambio en la organización; d) se incorporen actuaciones de refuerzo para asegurar el completo desarrollo de los proyectos de cambio. </li>
		</ol>
		<p>En este tenor, el cambio de las organizaciones públicas hacia un esquema de Gobierno
Abierto implica la sensibilización y apropiación social del conocimiento sobre qué es
y cuáles podrían ser sus finalidades por parte de los miembros de la organización
que busca ser reformada. </p>
		<p><strong>Sobre la innovación social</strong>. El modelo de gobernanza subyacente es uno de los
elementos clave de los procesos de innovación social, en la que medida que facilita
– o dificulta - los procesos de inclusión a partir de la creación de redes de
cooperación que involucran a agentes gubernamentales y de la comunidad, y
posibilita una mayor agilidad en los procesos de revelación de las necesidades
básicas. Sobre esta línea de argumentación, Morales, Barrera, Rodríguez, Romero,
y Távara (2014) consideran que son cuatro las características que toda iniciativa o
experiencia debe cumplir para considerarse como innovación, que son: </p>
		<ol>
			<li>Que sea generada y desarrollada por funcionarios de gobierno.<a href="#nota-1"><sup>1</sup></a></li>
			<li>Que la idea produzca cambios en los contenidos, las estructuras y los modos administrativos; lo cual requiere de la creatividad del funcionario que la propone.</li>
			<li>Que esté dirigida a la atención de necesidades concretas de la población beneficiaria.</li>
			<li>Que la idea coadyuve al desarrollo local.</li>
		</ol>
		<p>La perspectiva de innovación, en sus distintas facetas, se requiere en el modelo de
Gobierno Abierto tanto en la definición del ambiente en donde aterrizan sus
componentes (transparencia y participación), como para elaborar un plan para la
implementación de las acciones en particular.</p>
	</div>
