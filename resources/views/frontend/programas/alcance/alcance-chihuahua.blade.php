		<h2>Contexto</h2>
					<p>El 20 de junio de 2016, el estado de Chihuahua se integró formalmente a los Ejercicios Locales de Gobierno Abierto a través de la firma de la <a href="http://inicio.inai.org.mx/SiteCollectionDocuments/Transparencia/Chihuahua/Chihuahua.pdf">Declaración Conjunta para la Implementación de las Acciones para un Gobierno Abierto</a>.</p>
			<p>En esta Declaración firmada por un representante del Gobierno del estado, un representante de la sociedad civil y el Organismo Garante de Acceso a la Información, Chihuahua se comprometió a implementar políticas de apertura por medio de la institucionalización de órganos consultivos permanentes, metodologías para la identificación de problemáticas específicas y adoptar esfuerzos ciudadanos innovadores.</p>
			<p>El 29 de septiembre se sostuvo una reunión con dos magistrados del Tribunal Superior de Justicia de Chihuahua, en la cual se les orientó acerca de los pasos iniciales para la realización del ejercicio en el estado. En dicha oportunidad, se comentó un tema identificado en el que existe interés para que posteriormente se pueda establecer como un compromiso de Gobierno Abierto: igualdad en el acceso a la justicia para personas con alguna discapacidad.</p>
			<p>Los días 15 y 16 de noviembre se realizaron dos <a href="http://www.ichitaip.org/infoweb/noticias/muestra_noticia.php?id=405">Foros de Sensibilización en Chihuahua y Ciudad Juárez</a> respectivamente, dirigidos a altos funcionarios del estado y a sociedad civil en general. </p>
			<p>Posteriormente, el estado definirá su Plan de Trabajo para avanzar en los siguientes pasos del proceso que contempla el Ejercicio Local de Gobierno Abierto. </p>
		
