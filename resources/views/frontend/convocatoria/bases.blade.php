<div class="row">
	<div class="col-sm-10 col-sm-offset-1">
		<h1><strong>Proceso de selección de candidatos</strong> a convocatoria Programa de Formación de Agentes Locales de Cambio en Gobierno Abierto y Desarrollo Sostenible</strong>
		</h1>
		<p>Este procedimiento tiene por objeto brindar pautas generales para la elección de hasta 20 participantes para el <strong>Programa de Formación de Agentes Locales de Cambio en Gobierno Abierto y Desarrollo Sostenible</strong>, edición 2017. La convocatoria para participar en este programa estará abierta desde el 1° de marzo y hasta las 15:00 horas del 10 de abril de 2017 (tiempo de la Ciudad de México). Una vez cerrada la convocatoria, el proceso de selección de candidatos será realizado por el Comité Dictaminador, integrado por:</p>
		<ol>
			<li>Un representante de la oficina para México del Programa de Naciones Unidas para el Desarrollo.										  </li>
			<li>Un representante del Instituto Nacional de Transparencia, Acceso a la Información y Protección de Datos Personales.					  </li>
			<li>Un representante de la organización Gesoc, Agencia para el Desarrollo, A.C.															  </li>
			<li>Un representante de la organización ProSociedad, Hacer Bien el Bien, A.C.															  </li>
			<li>Un representante de la organización Gobierno Fácil																					  </li>
			<li>Un representante de la oficina para México de la Agencia de los Estados Unidos para el Desarrollo Internacional (USAID).			  </li>
			<li>Un académico especialista en temas de gobierno abierto y/o desarrollo sostenible, quien fungirá como testigo del proceso de selección.</li>
		</ol>
		<p>La selección de aspirantes se llevará a cabo exclusivamente con base en las postulaciones recibidas a través de la plataforma en línea creada para tal efecto, y se conformará por las siguientes etapas:</p>
	</div>
	<div class="col-sm-8 col-sm-offset-2">
		<ol>
			<li>
				<h3>Fase de revisión documental:</h3>
				<p>El Comité Dictaminador verificará – con apoyo del Coordinador del Proyecto – el	cumplimiento de los requisitos de la convocatoria. Durante esta etapa se revisará si:</p>
				<ul>
					<li>El aspirante proviene de alguna de las cinco entidades federativas objetivo (validado con la copia de comprobante de domicilio y la carta de identificación laboral). En caso de que el aspirante compruebe residencia en otra entidad federativa, deberá descartarse su postulación.</li>
					<li>El aspirante entregó en tiempo y forma el resto de la documentación y archivos solicitados en la convocatoria: hoja de registro, ensayo, video y perfil curricular. En caso de que un aspirante envíe la documentación completa, pero algún documento o archivo no pueda ser leído o abierto, se solicitará su envío de nueva cuenta al Comité Dictaminador. Toda postulación con documentación incompleta deberá ser descartada.
					</li>
				</ul>
				<p>Una vez concluida la revisión, se esperaría contar con, al menos, 9-10 aspirantes de cada una de las 5 entidades federativas objetivo (al menos 45-50 perfiles).</p>
				<p><strong>Duración estimada de esta etapa: del 10 al 12 de abril. </strong></p>
			</li>

			<li>
				<h3>Etapa de análisis de experiencia previa y propuesta de proyecto de los aspirantes:</h3>
				<p>En una segunda etapa, el Comité Dictaminador evaluará la experiencia previa de los candidatos preseleccionados en proyectos/actividades relacionados con temas de gobierno abierto y/o desarrollo sostenible, así como las propuestas de proyecto presentadas en el ensayo y el video. Cada uno de los componentes de esta fase se ponderará de la siguiente manera:</p>
				<ul>
					<li>Experiencia previa probada en proyectos de GA y/o DS: 30 por ciento.</li>
					<li>Ensayo escrito: 35 por ciento.</li>
					<li>Video de presentación: 35 por ciento.</li>
				</ul>
				<p>En cada uno de los elementos arriba mencionados, los integrantes del Comité Dictaminador evaluarán el manejo teórico y práctico de los conceptos de gobierno abierto y desarrollo sostenible; la capacidad de expresión escrita y oral de los aspirantes; así como la calidad y relevancia potencial de los proyectos presentados. Para esta etapa del proceso, los integrantes del Comité Dictaminador emplearán un formato de evaluación estandarizado (ver Anexo 1).</p>

				<p>Los aspirantes que obtengan los mejores puntajes, una vez sumadas las evaluaciones de todos los integrantes del Comité Dictaminador, pasarán a la fase de entrevistas. Una vez aplicado este segundo filtro, se esperaría contar con, al menos, 5-6 aspirantes de cada una de las 5 entidades federativas de interés (al menos 25-30 perfiles).</p>
				
				<p>Como parte de la evaluación de esta fase, el Comité Dictaminador deberá ponderar el equilibrio entre hombres y mujeres, así como entre candidatos provenientes de los sectores gubernamental y social.</p>

				<p><strong>Duración estimada de esta fase: Del 13 al 20 de abril.</strong></p>
			</li>

			<li>
				<h3>Etapa de entrevista:</h3>
				<p>A más tardar el 20 de abril, el Comité Dictaminador deberá informar a los 30 aspirantes preseleccionados sobre la fecha y la hora en la que se realizará la entrevista final para participar en el programa de fortalecimiento de capacidades. Cada entrevista tendrá una duración no mayor a 30 minutos, en donde se cuestionará al aspirante sobre temas clave de la agenda de gobierno abierto y desarrollo sostenible en lo Local en México. Para tal efecto, y como guía, se podrán utilizar las preguntas empleadas en las entrevistas para la contratación del coordinador de proyecto. Las fechas y horarios que se proponen para la realización de entrevistas es la siguiente:</p>
				<ul>
					<li>Martes 25 de abril: entrevista a 15 aspirantes (4 horas por la mañana y 3 por la tarde)	  </li>
					<li>Miércoles 26 de abril: entrevista a 15 aspirantes (4 horas por la mañana y 3 por la tarde)</li>
				</ul>
				<p>Para la valoración de las respuestas, los integrantes del Comité Dictaminador contarán con un formato estandarizado de evaluación. Los principales criterios que se evaluarán en esta fase del proceso son:</p>
				<ul>
					<li>Conocimiento del aspirante de los conceptos de gobierno abierto y desarrollo sostenible, y su aplicación en el ámbito local: 25 por ciento.	</li>
					<li>Capacidad de expresión y de síntesis de las características del proyecto que el aspirante busca desarrollar durante el programa: 25 por ciento.	¡</li>
					<li>Reconocimiento de los vínculos del aspirante con otros actores sociales y/o gubernamentales de su entidad que le permitan convertirse en un replicador formal de las capacidades adquiridas: 25 por ciento.</li>
					<li>Expresión oral general: 25 por ciento. </li>
				</ul>
				<p>Una vez concluidas las entrevistas, se integrarán los puntajes dados por todos los integrantes del Comité Dictaminador en esta fase, y se sumarán a los alcanzados por cada aspirante en la fase 2 (experiencia previa y propuesta de proyecto). En un primer momento, el Comité Dictaminador seleccionará respectivamente al hombre y a la mujer que obtengan el mayor puntaje agregado de cada entidad federativa objetivo. Se procurará que exista un balance entre perfiles gubernamentales y sociales.</p>
				<p>En adición a los 20 candidatos seleccionados, se hará una segunda lista de hasta 5 aspirantes (uno por entidad federativa) que hayan obtenido los puntajes más elevados pero que no hayan sido seleccionados a participar en el programa. Ésta será la lista de espera que podrá utilizarse en caso de que algún aspirante seleccionado rechace participar en el proyecto.</p>
				<p><strong>Publicación de resultados: 28 de abril</strong></p>
			</li>
		
		</ol>
	</div>
	<div class="col-sm-10 col-sm-offset-1">
		<h3>Principios</h3>
		<p>El presente proceso de selección será abierto y se llevará a cabo con base en los principios de honradez, transparencia e imparcialidad. En todas las etapas de selección se cuidará la igualdad de espacios y oportunidades para hombres y mujeres; y se contará con la asesoría de un especialista neutral ajeno del proyecto para resolver cualquier situación no prevista.</p>
