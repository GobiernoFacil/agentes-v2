		<h2>Introducción</h2>
		<p>Gobierno Abierto surge en arenas internacionales como una propuesta frente a los retos que enfrentan los gobiernos democráticos en el mundo: dificultades para resolver problemas y lograr compromisos a través de las estructuras políticas representativas, limitada capacidad de respuesta frecuentemente rebasada por crecientes necesidades de comunidades cada vez más exigentes, la búsqueda de legitimidad en el ejercicio del poder más allá de la arena electoral. El concepto de apertura se ha trasladado también, en breve tiempo, a muchas esferas de lo público como las de justicia abierta, municipio abierto, parlamento abierto, tribunal abierto, entre muchas otras. En esta conceptualización de apertura, sin embargo, se ha carecido de un acuerdo mínimo sobre su naturaleza, sus componentes y sus límites.
</p>
		<p>La pluralidad de agendas e iniciativas llevadas a la práctica en los órdenes
internacional, nacional y local, ha provocado que Gobierno Abierto se entienda de
distintas maneras en diferentes contextos. La riqueza generada por las experiencias
prácticas, como laboratorios para el desarrollo del concepto, es el aspecto positivo
de esta situación. La confusión, la implementación inadecuada, la simulación y la
desestimación de las virtudes de este concepto han sido los aspectos negativos de
esta indefinición.</p>
<p>Con el propósito de avanzar en la homologación de los principios y las prácticas de
Gobierno Abierto en México, desde el INAI y el Sistema Nacional, se considera
oportuno avanzar en la construcción de un modelo teórico que clarifique conceptos
y criterios que definan la sustancia de este término. Este modelo se construye a
partir de una definición prescriptiva de Gobierno Abierto, a saber: un esquema de
gestión y de producción de políticas públicas orientado a la atención y la solución
colaborativa de los problemas públicos con base en colegiados plurales y, en cuyo
trabajo, convergen la transparencia y la participación ciudadana como criterios
básicos, en un ambiente de rendición de cuentas e innovación social.</p>
		<p>El modelo aquí desarrollado sistematiza y sintetiza mucho de lo que hasta ahora se
ha trabajado sobre Gobierno Abierto en los órdenes conceptual y práctico. A manera
de base para la homologación de prácticas de Gobierno Abierto, su eventual
medición y el consecuente desarrollo de medidas para avanzar a un estado
deseable en nuestro país, este modelo ofrece un esquema que busca clarificar y
orientar. Más que la mera representación estática de una correlación de elementos, el modelo pretende ilustrar las características mínimas de un proceso de apertura
gubernamental que cualquier entidad, autoridad, órgano u organismo público puede
llevar a la práctica.</p>
		<p>La construcción y la difusión de un modelo para el desarrollo de acciones de
Gobierno Abierto no excluyen ni descalifican. Por el contrario, esta propuesta teórica
y práctica promueve la integración de una comunidad de aprendizaje a partir de un
acuerdo conceptual y su implementación en los distintos órdenes y, de manera
transversal, en entidades, órganos u organismos de naturaleza heterogénea. De
esta manera, el intercambio de experiencias y la coordinación de esfuerzos, a
manera de política nacional, es posible.</p>
		<p>Este documento se divide en tres secciones. En la primera de ellas se discuten
algunos elementos teóricos sobre la noción de Gobierno Abierto. En el segundo
apartado se describen brevemente algunos elementos de la práctica nacional e
internacional que han guiado el desarrollo del paradigma de Gobierno Abierto.
Finalmente, en la tercera sección se detallan los elementos mínimos del Modelo de
Gobierno Abierto propuesto desde el INAI y el Sistema Nacional, prestando
particular atención a los rasgos que deberían caracterizar a cualquier institución
guiada por principios de apertura gubernamental.</p>
