		<h2>ii. El desarrollo del concepto de Gobierno Abierto desde la práctica internacional y nacional</h2>
		<p>Desde sus orígenes, Gobierno Abierto se ha construido a partir de líneas teóricas muy generales que motivaron el desarrollo de prácticas muy diversas que, a su vez, nutrieron esquemas y propuestas conceptuales que se propagaron y evolucionaron en distintos contextos y velocidades. Al día de hoy, el implementador de Gobierno Abierto enfrenta un dilema para decidir cuál esquema de los existentes adoptar.</p>
		<p>Las experiencias observadas en diversos contextos y ámbitos han mostrado que no basta con la definición de un esquema o ilustración genérica que sugiera o explique un estado deseable de cosas, sino que debe avanzarse en la clarificación de sus elementos específicos. Al respecto, Galofré Isart señala que <em>“no han faltado, a lo largo de los últimos años, reformadores bien intencionados que han pensado que era suficiente con definir sobre el papel un nuevo modelo teórico y ordenar su aplicación para que el cambio se produjera”</em> (1997:15). La posibilidad de alcanzar un estado deseable – en este caso, un nuevo modelo de gobernanza - requiere un esfuerzo por ubicar lo conceptual en la práctica. Con tal intención, a continuación se describen algunos momentos clave de la experiencia de lo que ha implicado llevar a la práctica Gobierno Abierto.</p>
		<h3>Gobierno Abierto, antes de la Alianza para el Gobierno Abierto <a href="#nota-2"><sup>2</sup></a></h3>
		<p>El Gobierno Abierto, en su versión actual, viene directamente de la administración del presidente estadounidense Barack Obama. En esta experiencia son tres los hitos identificables: los dos primeros definidos a partir de los documentos “Transparencia y Gobierno Abierto” de enero de 2009 y “Directriz para el Gobierno Abierto” de diciembre de ese mismo año. El tercero de ellos comienza en 2011, cuando el mandatario estadounidense presenta, en la Asamblea General de las
Naciones Unidas, el Open Government Partnership que en este documento se identifica como Alianza para el Gobierno Abierto (AGA).</p>
		<p>Al inicio de su primera administración, el presidente Obama firmó el memorándum “Transparencia y Gobierno Abierto” expresando el compromiso de crear un nivel de apertura gubernamental sin precedentes a partir de tres principios básicos (Obama, The White House, 2009):</p>
		<ol>
			<li>El gobierno debe ser transparente, como característica que promueva la rendición de cuentas;</li>
			<li>El gobierno debe ser participativo, como medio para incrementar la efectividad y calidad de sus decisiones aprovechando el conocimiento que se encuentra disperso entre su gente;</li>
			<li>El gobierno debe ser colaborativo, usando herramientas, sistemas y métodos innovadores que promuevan el intercambio y retroalimentación en todos los niveles y considerando las organizaciones sin fines de lucro, negocios e individuos del sector privado.</li>
		</ol>
		<p>En el documento antes citado se instruye a diversas oficinas al interior de la Presidencia para que, en un plazo de 120 días, lleven estos postulados a la práctica. En diciembre de 2009, el director de la Oficina de Administración y Presupuesto<a href="#nota-3"><sup>3</sup></a> de la Presidencia de Estados Unidos (OMB, por sus siglas en inglés), Peter R. Orszag, envió el memorándum M-10-06.<a href="#nota-4"><sup>4</sup></a> En dicho documento, los pilares del Gobierno
Abierto son la transparencia, la participación y la colaboración (Orszag, 2009). De acuerdo con este memorándum, son cuatro los pasos necesarios para crear un Gobierno Abierto:</p>
		<ol>
			<li>Publicar en Internet información gubernamental;					 </li>
			<li>Mejorar la calidad de la información ya publicada;				 </li>
			<li>Crear e institucionalizar una cultura de Gobierno Abierto;		 </li>
			<li>Crear y difundir un marco para las políticas de Gobierno Abierto.</li>
		</ol>
		<p>Este documento atiende al contexto existente en la administración pública norteamericana y, a partir de ahí, propone una ruta crítica para su implementación. La publicación en Internet de la información del gobierno, por ejemplo, se concibe como una oportunidad para incrementar la rendición de cuentas, para promover la participación informada y para crear oportunidades para la economía.<a href="#nota-5"><sup>5</sup></a> De aquí, los compromisos son: 
que cada agencia publique, en formato abierto, al menos tres bases de datos de alto valor registrándolas en un sitio único que los concentraría;<a href="#nota-6"><sup>6</sup></a> que cada agencia lance un portal de Gobierno Abierto localizado en una dirección estandarizada (www.[nombre de la agencia].gov/open), señalando los mecanismos de comunicación y retroalimentación con la ciudadanía. A esto se le denominó “Plande Gobierno Abierto” particular de la agencia. </p>
		<p>Para mejorar la calidad de la información gubernamental, el memorándum M-10-06 hace referencia a la normatividad ya existente y al acompañamiento constante de la OMB.<a href="#nota-7"><sup>7</sup></a> En este paso también se definen actividades específicas sujetas a un
tiempo para su implementación: designar a un funcionario de alto nivel como el
responsable de la calidad y la objetividad de la información; establecer un marco
básico para evaluar la calidad y publicar la información del gasto.<a href="#nota-8"><sup>8</sup></a></p>

		<p>La institucionalización de la cultura de Gobierno Abierto se definió como un trabajo multidisciplinar al interior de las agencias gubernamentales. En el documento se estableció una ruta crítica para hacerlo, para gestionar el cambio de la cultura organizacional. Un último paso que se definió fue la creación y difusión de un marco para las políticas de Gobierno Abierto. Aquí el asunto se aborda de manera más general: se establece que a los 120 días la Oficina de Asuntos de Información y Regulación habrá revisado cuáles de los trámites existentes, podrán ser automatizados e integrados a una plataforma tecnológica.</p>
		
		<p>El segundo de los apartados del memorándum M-10-06 describe los contenidos mínimos de un Plan de Acción que, de acuerdo con el documento, deben incorporar el parecer de un funcionario senior del área jurídica, tecnológica o policy y el público en general o expertos en apertura gubernamental. La estructura propuesta podía mejorarse con la incorporación del ciudadano y los servidores públicos durante el proceso. En virtud de la transparencia, en el Plan debería establecerse cómo ésta será incrementada por la agencia a través de la valoración de sus usuarios y poniéndola a disposición para ser aprovechada por el ciudadano común. La transparencia, en este esquema, está relacionada con la rendición de cuentas (<em>accountability</em>) y la capacidad de respuesta (<em>responsiveness</em>) de la agencia.</p>
		<p>En cuanto a participación, el Plan debería describir la revisión que haga la agencia para encontrar áreas de oportunidad y, cuando se requiera, el rediseño de mecanismos de retroalimentación del público. En el aspecto de la colaboración, la agencia debe detallar: </p>
		<ol>
			<li>Cómo mejorar la comunicación con personas fuera de la agencia mediante el uso de las plataformas tecnológicas;</li>
			<li>Cómo establecer algún espacio (un sitio web) donde puedan conocerse actividades existentes de colaboración y aprender de ellas, y </li>
			<li>Cómo establecer métodos innovadores (competencias, premios) para incentivar la colaboración.</li>
		</ol>
		<p>Finalmente, el Plan de Acción debe contener una iniciativa insignia (<em>flagship initiative</em>), donde se deja a cada agencia la construcción de, al menos, una medida que contribuya en alguno de los pilares del Gobierno Abierto. </p>
		
		<h3>Gobierno Abierto, posterior a la Alianza para el Gobierno Abierto</h3>
		<p>Para septiembre de 2010, dentro del marco de la Asamblea de las Naciones Unidas,
el presidente Obama señaló: “Como dije el año pasado, cada país seguirá el camino
enraizado en la cultura de su pueblo. Sin embargo, la experiencia nos enseña que
el progreso de la humanidad descansa en la economía abierta, la sociedad abierta,
el gobierno abierto” (Obama, Council on Foreign Relations, 2010). Ahí mismo,
propuso regresar al año siguiente con propuestas específicas para promover la
transparencia, combatir la corrupción, avivar el compromiso ciudadano y apalancar
el uso de la tecnología para fortalecer los cimientos de la libertad en los países.</p>
		<p>Como consecuencia de lo anterior, en 2011 se constituyó la Alianza para el
Gobierno Abierto, como iniciativa multilateral que apoyaría los esfuerzos nacionales
para promover la transparencia, combatir la corrupción, fortalecer la rendición de
cuentas y empoderar al ciudadano (<em>The White House. Office of the Press Secretary</em>,
2011). El documento fundacional, <em>Open Government Declaration</em>, establece los
siguientes compromisos: </p>
		<ol>
			<li>Incrementar la disponibilidad de la información gubernamental;																</li>
			<li>Apoyar la participación cívica;																								</li>
			<li>Implementar altos estándares de integridad profesional en el servicio público;												</li>
			<li>Incrementar el acceso a nuevas tecnologías para la apertura y la rendición de cuentas (Open Government Partnership, 2011). 	</li>		
		</ol>
		<p>De 2011 a 2015, el número de países miembros de AGA ha pasado de 8 a 69. Dada
la heterogeneidad de los países participantes, AGA no promueve un esquema único
de Gobierno Abierto, sino un marco general de lo que debiera entenderse por él
(Transparency and Accountability Initiative, 2015). Esta Alianza ofrece una
definición de trabajo basada en la propuesta hecha por Nathaniel Heller (2012), a
partir de los siguientes elementos: transparencia informativa (incluyendo iniciativas
sobre libertad de información, <em>Open Data</em>, <em>Big Data</em>); compromiso público (servicios
de gobierno electrónico, presupuestos participativos, procesos electorales); y
rendición de cuentas (mecanismos anticorrupción, auditorías). El autor enfatiza que
los componentes por sí solos no son Gobierno Abierto, se requiere la combinación
de ellos. Sobre el uso de la tecnología expresamente señala que no es un
requerimiento para la existencia de Gobierno Abierto (Heller, 2012). </p>
		
		<h3>INAI y el impulso de un laboratorio de apertura gubernamental en el ámbito local</h3>
		<p>Formalmente, no hay un camino establecido para que una entidad local pertenezca
a la AGA (Open Government Partnership, 2015).<a href="#nota-9"><sup>9</sup></a> La Alianza alienta, sin embargo,
su integración como parte de los compromisos nacionales. En México se ha emprendido un esfuerzo importante y sin precedentes para llevar el llamado
Gobierno Abierto al ámbito local. Se coordina desde el Instituto Nacional de Transparencia, Acceso a la Información y Protección de Datos Personales (INAI) y
es, sin duda, una experiencia de la que podrá (y deberá) aprenderse al mismo tiempo que valorar sus consecuencias.</p>
		<p>Desde la perspectiva de políticas públicas, la iniciativa de llevar al orden local generó un interesante laboratorio cuyo conocimiento y análisis es importante tanto para el entendimiento del tema, en lo específico, como para la comprensión del funcionamiento del federalismo en la implementación de una política diseñada desde el centro.</p>
		<p>El punto de origen se ubica en 2014, cuando el INAI<a href="#nota-10"><sup>10</sup></a> y dos comisiones especializadas de la Conferencia Mexicana para el Acceso a la Información Pública (COMAIP)<a href="#nota-11"><sup>11</sup></a> desarrollaron una estrategia para avanzar en la consolidación de un Estado Abierto (IFAI-COMAIP, 2015). Parte de ello derivó en la organización de ejercicios locales a implementarse en dos etapas semestrales, a lo largo de 2015.
En marzo de 2015, en las instalaciones del INAI, se celebró el evento “Gobierno
Abierto. Co creación desde lo local” donde tuvo verificativo una firma protocolaria
del compromiso que asumían entidades federativas en cuanto a los ejercicios
locales de Gobierno Abierto. Posterior a dicha reunión, el INAI emprendió en
paralelo, la construcción de un modelo de política pública que permitiera avanzar
esta agenda con otros actores públicos, por lo que realizó una reunión con expertos
para recabar opiniones y observaciones. De este encuentro derivó un documento
preliminar de trabajo que se convirtió posteriormente en un insumo del presente
producto.</p>
		<p>En el orden estatal, la experiencia de co creación local ya ha generado un catálogo
de prácticas que enriquecen la discusión en cuanto a la manera en la que los
contextos influyen en la implementación. En Coahuila, por ejemplo, la integración del cuerpo colegiado que involucra al sector público, el organismo garante y la
ciudadanía, se estableció por región dada la extensión territorial o en Baja California,
donde el Congreso Estatal asumió la agenda de Gobierno Abierto
complementándola con otra práctica denominada Parlamento Abierto. En Durango,
se innovó la base de la organización (el Secretariado Técnico Local) mediante la
figura de la vocalía, que permite mayor representación y capacidad de incidencia
social. En Zacatecas, la instancia de coordinación (Secretariado Técnico Local) se
integra por representantes de la Federación de Colegios y Asociaciones de
Profesionistas de Zacatecas, el Gobierno del Estado, La Legislatura del Estado, el
Tribunal Superior de Justicia, la Universidad Autónoma del Estado, el Ayuntamiento
de Zacatecas y la Comisión Estatal para el Acceso a la Información Pública. Como
se observa, en estas experiencias, las adecuaciones a la propuesta original se
hacen presentes tomando diversas formas. </p>
		<h3>Recapitulación</h3>
		<p>Llevar a la práctica Gobierno Abierto requiere un concepto que propicie la
integración de una comunidad de aprendizaje y el diálogo permanente. El aprecio
de las capacidades de los implementadores (los que laboran en el sector público y
las personas que desde el sector social o ciudadano se integran) y su conocimiento
del contexto en el cual se lleva a cabo, son piezas fundamentales en el éxito de
llevar un concepto a la realidad.</p>
		<p>Una claridad conceptual también coadyuva a dimensionar la incidencia de Gobierno
Abierto. Es clara la necesidad de vinculársele con la solución o alivio de
problemáticas concretas y propiciar su desarrollo de manera coordinada con otros
esfuerzos como los de la fiscalización o la rendición de cuentas. Visto de esta
manera, Gobierno Abierto no es un fin en sí mismo sino el medio para potenciar lo
ya emprendido. Esta visión es complementaria a quienes observan en la apertura,
y los atributos inherentes a ella, el fin de la política pública.</p>
		<p>Del breve recorrido realizado, se destaca lo siguiente:</p>
		<ol>
			<li>El surgimiento de Gobierno Abierto, con el impulso de la AGA, es una propuesta general desarrollada para que cada país implementador delimite y acuerde las características para hacerlo;</li>
			<li>Forma parte de los esfuerzos que pretenden reconfigurar la relación entre gobierno y gobernado en el sentido más amplio de la expresión: los que toman las decisiones que inciden en la esfera pública y sobre quienes esas decisiones vierten sus consecuencias.</li>
			<li>La experiencia estadounidense refleja una labor por darle sentido a la expresión Gobierno Abierto y, al mismo tiempo, señalar pasos y requisitos puntuales para el desarrollo de una política nacional;</li>
			<li>El elemento relacionado con el uso de las tecnologías de la información, aunque constante, no aparece como esencial para la existencia de Gobierno
Abierto. Su presencia se explica en una narrativa adicional, la de la cobertura y los bajos costos que, en algunas soluciones, puede ofrecer;</li>
			<li>En los ejes de transparencia y participación parece haber consenso; el tercero de los ejes cambia: en ocasiones es rendición de cuentas, en otros,
corresponsabilidad (entre los más comunes).</li>
		</ol>
		<p>Gobierno Abierto importa en la medida en la que promueve la solución o alivio a
problemas identificados por una comunidad como importantes. El modelo propuesto
tiene ese énfasis: más allá de la discusión teórica, debe poderse llevar a la práctica
por un amplio universo de entidades, órganos u organismos, diferentes intensidades
de participación y contextos variados.</p>
		<p>Gobierno Abierto requiere de un trabajo al interior de las entidades, órganos u
organismos implementadores. Requiere, también, de una participación de calidad
de parte de la comunidad o ciudadanía. Es necesario acompañar el proceso con un
esfuerzo de sensibilización, capacitación y divulgación de lo que Gobierno Abierto
implica por sí mismo y lo que significa en la recomposición de la relación entre
quienes gobiernan y sobre quienes esas decisiones recaen.</p>
	</div>
	
	<div class="col-md-8 col-sm-10 col-md-offset-2 col-sm-offset-1">
		<h2>iii. Modelo de Gobierno Abierto</h2>
		<p>El presente Modelo parte de una definición normativa de Gobierno Abierto que
entiende este concepto como:</p> 
		<blockquote> un esquema de gestión y de producción de políticas
públicas orientado a la atención y la solución colaborativa de los problemas públicos
con base en colegiados plurales y, en cuyo trabajo, convergen la <strong>transparencia</strong> y
la <strong>participación ciudadana</strong> como criterios básicos, buscando propiciar un
ambiente de <strong>rendición de cuentas</strong> e <strong>innovación social</strong>.</blockquote>
		<p>El concepto de Gobierno Abierto aquí propuesto está orientado a la acción y la
implementación ya que, además de definir sus componentes mínimos, permite
orientar las decisiones de los implementadores de acuerdo con sus propias
realidades, capacidades, recursos y fines. A pesar de esto, esta concepción con
relación al Gobierno Abierto enfatiza la necesidad de reorientar el modelo de
gobernanza y las pautas de las instituciones públicas bajo estándares claros de
transparencia y participación ciudadana, así como al desarrollo de una cultura
organizacional caracterizada por la rendición de cuentas y la innovación social
constante. </p>
		<p><img src="{{url('img/esquema-gobierno-abierto.png')}}" alt="Esquema de gobierno abierto"></p>
		<p>Más que imponer un esquema único de implementación, este Modelo considera que
las organizaciones públicas pueden ir avanzando en su proceso de apertura
gubernamental a partir del cumplimiento – y el mantenimiento - de estándares cada
vez más estrictos de transparencia y de participación ciudadana. En el primer caso,
con la puesta en marcha de este Modelo se esperaría que las instituciones
gubernamentales avancen en la instrumentación e institucionalización de esquemas
comprensivos de divulgación que no sólo pongan a disposición de la ciudadanía la
información exigida por la legislación o solicitada ex profeso (transparencia
reactiva), sino también la publicación de datos e información socialmente útil
(transparencia proactiva).</p>
		<p><img src="{{url('img/dimension-gobierno-abierto.png')}}" alt="Dimensión de Transparencia de Gobierno Abierto"></p>
		<p>En el segundo caso, con la implementación de este Modelo de Gobierno Abierto se
espera que las instituciones gubernamentales construyan una arquitectura
participativa amplia integrada por instrumentos de participación con alcances
diversos (consulta, involucramiento, colaboración y vigilancia social), y que permita
a los diferentes públicos relevantes de la institución (beneficiarios, usuarios,
organizaciones y expertos) incidir en las distintas fases del proceso de formulación
e implementación de las políticas públicas. </p>
		<p><img src="{{url('img/arquitectura-participacion.png')}}" alt="Arquitectura de la Participación en Gobierno Abierto"></p>
		<p>Con lo anterior, este Modelo asume que una institución pública – de cualquier orden
- que oriente su actuar bajo principios de apertura gubernamental debería,
simultáneamente, implementar y mantener prácticas robustas de transparencia
proactiva, así como esquemas comprensivos de participación ciudadana que
incidan sobre la forma como se toman decisiones en dicha organización. El sostenimiento de estos dos elementos propiciaría la configuración de instituciones
gubernamentales capaces de rendir cuentas sobre sus acciones en todas las fases
del proceso de producción de políticas, comprometidas con la colaboración y la co
creación constante con la ciudadanía, y que fomentan la innovación y la búsqueda
de nuevas soluciones a viejos problemas.</p>
		<p>Con relación a este último punto, en este Modelo se considera que el ambiente de
rendición de cuentas se propiciaría a través de la realización reiterada de ejercicios
de Gobierno Abierto transparentes y participativos que, paulatinamente, son
asimilados a la práctica habitual de las organizaciones gubernamentales. Bajo esta
lógica, con el establecimiento de planes de trabajo y de compromisos de apertura
gubernamental: a) obligan a los funcionarios públicos a informar, explicar y justificar
su conducta y decisiones a sus públicos relevantes, y b) definen espacios de
colaboración y de seguimiento concretos que permiten examinar puntualmente el
logro de compromisos y de metas.</p>
		<p>Por su parte, la instrumentación de acciones y de compromisos guiados bajo los
principios de Gobierno Abierto propuestos en este Modelo tienen la capacidad de
promover un ambiente de innovación dentro de las instituciones gubernamentales,
ya que:</p> 
		<ol>
			<li>los problemas públicos priorizados a través de mecanismos participativos pueden reflejar, de mejor forma, situaciones problemáticas o consideradas como valiosas por los públicos relevantes para las que no se han encontrado soluciones a través de los canales tradicionales; y </li>
			<li>las acciones y los compromisos asumidos pueden ser atendidos a través de herramientas (tecnológicas o colaborativas) que pueden reducir costos y agilizar su implementación.</li>
		</ol>
		<p><img src="{{url('img/componente-participacion.png')}}" alt="Componente de Participación en Gobierno Abierto"></p>
		<p>Con base en estos elementos generales, este Modelo sugiere dos instrumentos que
cualquier institución gubernamental – sin importar su orden - debería poner en
marcha para avanzar en la definición y la implementación de una estrategia de
Gobierno Abierto. Estos instrumentos – cuyos alcances se detallaran con mayor
precisión en las respectivas Guías de Implementación - son los siguientes:</p>
		<ol>
			<li>Un Comité de Apertura que se concibe como un espacio de participación – integrado, mínimamente por un representante de la dependencia, del organismo garante y de los públicos relevantes - encargado de coordinar los procesos internos y externos orientados a identificar y priorizar problemas y temáticas susceptibles de ser atendidos bajo un esquema de apertura gubernamental.</li>
			<li>Una Agenda Compartida de Gobierno Abierto que se concibe como el documento básico en el que para cada dependencia - y como producto de diagnósticos internos y externos realizados por el Comité de Apertura - se detallen los compromisos y actividades que asumirá y realizará el sujeto obligado con base en los principios de apertura gubernamental.</li>
		</ol>
