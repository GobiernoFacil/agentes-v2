		<h2>Contexto</h2>
		<p>El 9 de marzo de 2015, el estado de Tlaxcala se integró formalmente a los Ejercicios Locales de Gobierno Abierto a través de la firma de la <a href="http://inicio.ifai.org.mx/doc/DGGAT/LGTAP70XLVIIID/FirmaDeclaratoriaTlaxcalareu.pdf">Declaración Conjunta para la Implementación de Acciones para un Gobierno Abierto</a>.</p>
		<p>En esta Declaración, firmada por un representante de gobierno municipal, un representante de la sociedad civil y el Organismo Garante de Acceso a la Información y Protección de Datos Personales en la entidad, Tlaxcala se comprometió a establecer actividades y trabajos encaminados a consolidar y expandir la apertura institucional y la participación ciudadana.</p>
		<p>Luego de la firma de la Declaración Conjunta, en Tlaxcala se realizaron jornadas de sensibilización con organizaciones de la sociedad civil y público en general, donde se informó y socializó el ejercicio Gobierno Abierto “Co-creación desde lo Local” y se capacitó a servidores públicos que mostraron interés hacia el proyecto.</p>
		<p>Posteriormente, el 7 de julio de 2015 se instaló el Secretariado Técnico en la entidad, mismo que coordino las actividades para la integración del Plan de Acción Local, mismas que consistieron en la celebración de reuniones del Secretariado, mesas de diálogo, mesas de trabajo y trabajos de Parlamento Abierto en el Congreso del Estado.</p>
		<p>Este proceso dio como resultado que el 30 de junio de 2016 se publicara el <a href="http://inicio.ifai.org.mx/doc/DGGAT/LGTAP70XLVIIID/PALTlaxcalareu.pdf">Plan de Acción Local</a>. El documento está integrado por 5 compromisos puntuales, realizables, relevantes y medibles, con duración de un año:</p>
		<ul>
			<li><strong>Compromiso 1:</strong> Dotar a las adolescentes de mejores herramientas de información que les permitan prevenir embarazos, además de vincular a las jóvenes que se encuentren embarazadas con instituciones que brinden apoyos educativos, con la finalidad de generar una oportunidad para que continúen con su preparación educativa.</li>
			<li><strong>Compromiso 2:</strong> Incentivar la participación ciudadana por medio de la creación de dos consejos ciudadanos de obra pública, y establecer lineamientos administrativos necesarios y específicos para la opinión de las y los ciudadanos, el diálogo con servidores públicos y vigilancia de la ejecución de obra pública. Además de dotar a la población del municipio de Ixtenco con información sobre la ejecución de recursos públicos para fomentar la transparencia y la rendición de cuentas.</li>
			<li><strong>Compromiso 3:</strong> Establecer una ruta específica conforme a los lineamientos del programa follow the money, sobre la ruta de los recursos públicos del Fondo para el Fortalecimiento Municipal (FORTAMUN) y el Fondo de Infraestructura Social Municipal (FISM), en los municipios de Xicohtzinco y Sanctórum de Lázaro Cárdenas respectivamente, para saber el manejo que le dan las autoridades respectivas para la ejecución de los mismos.</li>
			<li><strong>Compromiso 4:</strong> El municipio de Ixtacuixtla de Mariano Matamoros sufre de contaminación en sus barrancas, siendo las familias que habitan en esos lugares las principales afectadas. Motivo por el cual se requiere concientizar a la población del municipio de Ixtacuixtla sobre este problema, para que dejen de tirar basura y participen activamente en campañas para cuidar el medio ambiente.</li>
			<li><strong>Compromiso 5:</strong> Dotar a las y los ciudadanos de instrumentos electrónicos de innovación civil que permitan a la sociedad en general participar respecto a las actividades que desarrolla el Congreso, para fortalecer la inclusión social por medio de la participación ciudadana, fomentar el acceso a la información, la transparencia y la rendición de cuentas.</li>
		</ul>
