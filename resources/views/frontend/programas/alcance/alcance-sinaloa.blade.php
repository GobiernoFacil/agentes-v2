		<h2>Contexto</h2>
		<p>El 20 de junio de 2016, el estado de Sinaloa se integró formalmente a los Ejercicios Locales de Gobierno Abierto a través de la firma de la <a href="http://inicio.ifai.org.mx/doc/DGGAT/LGTAP70XLVIIID/FirmaDeclaratoriaSinaloareu.pdf">Declaración Conjunta para la Implementación de Acciones para un Gobierno Abierto</a>.</p>
		<p>En esta Declaración, firmada por un representante del Gobierno del Estado, un representante de la sociedad civil y el Organismo Garante de Acceso a la Información y Protección de Datos Personales en la entidad, Sinaloa se comprometió a establecer actividades y trabajos encaminados a consolidar y expandir la apertura institucional y la participación ciudadana.</p>
		<p>Luego de la firma de la Declaración Conjunta, en Sinaloa se han impartido tres sesiones de sensibilización con organizaciones de la sociedad civil y público en general, donde se informó y socializó el ejercicio Gobierno Abierto “Co-creación desde lo Local” y se capacitó a servidores públicos que mostraron interés hacia el proyecto.</p>
		<p>Posteriormente, el 18 de abril de 2017 se instaló el Secretariado Técnico en la entidad, mismo que tiene la encomienda de coordinar el proceso de construcción del primer Plan de Acción en la entidad.</p>
		<!---Estatus-->	
		<h2>Estatus</h2>
