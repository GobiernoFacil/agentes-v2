			<li>Asimismo, es importante señalar que, en cumplimiento del artículo 70, fracción XV, inciso q) de la Ley General de Transparencia y Acceso a la Información Pública se harán públicos los nombres de los candidatos seleccionados. Se informa que no se recabarán datos sensibles.</li>

